         Daopension pDAO = new Daopension();
         Pension p = new Pension();
         PrestataireDAO prestDAO = new PrestataireDAO();

        String nom = jTextField1.getText();
        String adr = jTextField2.getText();
        int tel = Integer.parseInt(jTextField3.getText().toString());
        String nom_prest = jComboBox1.getSelectedItem().toString() ;
        System.out.println("NOM prest "+nom_prest);
        int id_prest= prestDAO.getIDPrestataireByName(nom_prest);
        System.out.println("ID PREST "+id_prest);
        p.setNom(nom);
        p.setAdresse(adr);
        p.setTelephone(tel);
        p.setId_prest(id_prest);
        pDAO.insertPension(p);
        JOptionPane.showMessageDialog(this, "Ajout de la pension est effectué avec succés");

