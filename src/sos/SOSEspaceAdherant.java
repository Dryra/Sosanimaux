    private void chargementMap() {
        //recuperation de la liste des animaux
        String requete = "SELECT `animal`.`id`,`animal`.`nom`,`animal`.`espece`,`animal`.`age`,`animal`.`race`,`animal`.`poids`,`animal`.`couleur`,`animal`.`vaccin`,`animal`.`etat`,`animal`.`autre`,`animal`.`sexe`,`animal`.`adr`,`adresse`.`lat`,`adresse`.`lon`FROM `sosanimaux`.`animal`,`sosanimaux`.`adresse`where `animal`.`adr`=`adresse`.`ville`;";
        try {
            java.sql.PreparedStatement ps = MyConnection.getInstance().prepareStatement(requete);
            ResultSet resultat = ps.executeQuery();
            while (resultat.next()) {
                MapMarkerDot ville = new MapMarkerDot(resultat.getString("adr"), new Coordinate(resultat.getDouble("lat"), resultat.getDouble("lon")));
                mapAnimal.addMapMarker(ville);
            }
        } catch (SQLException ex) {
            //Logger.getLogger(PersonneDao.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("erreur lors du chargement des villes " + ex.getMessage());
        }
    }

